CREATE TABLE Player_219(
    SLNO NUMBER(10),
    Player_Id NUMBER(10) PRIMARY KEY,
    Lname VARCHAR2(20),
    Fname VARCHAR2(20),
    Country VARCHAR2(20),
    Yborn NUMBER(10),
    Bplace VARCHAR2(20),
    Ftest NUMBER(10)
);

CREATE TABLE Match_219(
    SLNO NUMBER(5),
    Match_Id NUMBER(10) PRIMARY KEY,
    Team1 VARCHAR2(20),
    Team2 VARCHAR2(20),
    Ground VARCHAR2(20),
    Play_Date DATE,
    Winner VARCHAR2(20)
);

CREATE TABLE Bowling_219(
    SLNO NUMBER(5),
    Match_Id NUMBER(10) REFERENCES Match_219(Match_Id),
    Player_Id NUMBER(10) REFERENCES Player_219(Player_Id),
    Novers NUMBER(10),
    Maidens NUMBER(10),
    Nrun_rcv NUMBER(10),
    Nwickets NUMBER(10)
);

CREATE TABLE Batting_219(
    SLNO NUMBER(10),
    Match_Id NUMBER(10) REFERENCES Match_219(Match_Id),
    Player_Id NUMBER(10) REFERENCES Player_219(Player_Id),
    Nrun_sc NUMBER(10)
);

